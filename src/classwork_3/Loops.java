package classwork_3;

public class Loops {

    static void printAll(int[] tver) {
        for (int i = 0; i < tver.length; i++) {
            System.out.println(tver[i]);
        }
    }


    static void plusOne(int[] tver){
        for (int i=0;i< tver.length;i++){
            System.out.println(tver[i]);
            System.out.println(tver[i] + 1);
            System.out.println("-------------------------");
        }
    }


}
